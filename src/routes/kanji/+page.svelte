	<h1>About Kanji</h1>
<main>
	<p>Since ancient times, there has been a strong opinion in Japan that kanji is the orthodox form of writing, but others have argued against it. Kamo no Mabuchi, a scholar of the Edo period, criticized the large number of characters in kanji. He also appreciated the small number of characters in kana characters and argued for the limitation of kanji.</p>
	<p>After the Meiji Restoration and as Japan entered an era of active exchange with foreign countries, the need for script reform in Japan began to be called for. Some scholars argued for the abolition of kanji and the writing of Japanese using only kana or Latin characters. These views were not widespread.</p>
	<p>However, the need to limit the number of kanji characters was understood, and in May 1923, the Japanese government announced 1,962 kanji characters for regular use. In 1940, the Japanese Army decided on the "Table of Restricted Kanji for Weapons Names" (兵器名称用制限漢字表, heiki meishō yō seigen kanji hyō) which limited the number of kanji that could be used for weapons names to 1,235.</p>
	<p>In 1942, the National Language Council announced the "Standard Kanji Table" (標準漢字表, hyōjun kanji-hyō) with a total of 2,528 characters, showing the standard for kanji used by ministries and agencies and in general society.</p>
	<p>In 1946, after World War II and under the Allied occupation of Japan, the Japanese government, guided by the Supreme Commander of the Allied Powers, instituted a series of orthographic reforms, to help children learn and to simplify kanji use in literature and periodicals.</p>
	<p>The number of characters in circulation was reduced, and formal lists of characters to be learned during each grade of school were established. Some characters were given simplified glyphs, called shinjitai (新字体). Many variant forms of characters and obscure alternatives for common characters were officially discouraged.</p>
	<p>These are simply guidelines, so many characters outside these standards are still widely known and commonly used; these are known as hyōgaiji (表外字).</p>
</main>

